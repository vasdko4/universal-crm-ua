-- Товары: включатель вариантов (цвет/размер и т.д.)
--
-- Раньше признаком «у товара варианты» служило просто наличие строк
-- в product_variants — это работало, но админ мог случайно оставить
-- заполненную матрицу вариантов, даже не собираясь её использовать, и
-- цена/остаток товара молча подменялись агрегатами по вариантам без
-- явного индикатора в интерфейсе. Явный флаг + блокировка полей
-- "Цена и остатки" в форме товара делают это поведение прозрачным.
ALTER TABLE "products" ADD COLUMN IF NOT EXISTS "variants_enabled" boolean DEFAULT false NOT NULL;

-- Товары, у которых уже настроены комбинации вариантов, включаем
-- автоматически — иначе после апдейта их цена/остаток резко переключились
-- бы обратно на старые значения базового товара.
UPDATE "products" p
SET "variants_enabled" = true
WHERE EXISTS (SELECT 1 FROM "product_variants" pv WHERE pv."product_id" = p."id")
  AND NOT p."variants_enabled";

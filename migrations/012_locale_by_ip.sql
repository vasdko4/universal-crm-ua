-- Язык витрины: запоминаем выбор по IP посетителя, чтобы не переспрашивать
-- повторно в другом браузере/устройстве той же сети.
-- Осознанное ограничение: IP бывает общим на нескольких людей (офис, семья,
-- мобильный оператор) и меняется при смене сети — это подсказка по
-- умолчанию, а не точная персональная привязка.
CREATE TABLE IF NOT EXISTS "locale_by_ip" (
  "ip" varchar(64) PRIMARY KEY,
  "locale" varchar(5) NOT NULL,
  "updated_at" timestamptz DEFAULT now() NOT NULL
);
